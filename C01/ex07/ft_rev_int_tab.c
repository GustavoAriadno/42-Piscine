void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int aux[size];

	i = 0;
	while (i < size)
	{
		aux[(size -i) - 1] = tab[i];
		i++;
	}
	while (i-- > 0)
	{
		tab[i] = aux[i];
	}
}
